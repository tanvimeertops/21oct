package com.basics;
/*
 * Condition operator :
 * if: 
 * if condition true, true statement print else nothing
 * if else 
 * if condition is true, true statement print
 * else false statement print
 * nested if
 * else if ladder
 * switch
 */
public class ConditionDemo {
public static void main(String[] args) {
	int a=10,b=20;
	
	if(a>b) {
		System.out.println("a is smaller than b");
	}else {
		System.out.println("a is greater than b");
	}
	
	
}
}
